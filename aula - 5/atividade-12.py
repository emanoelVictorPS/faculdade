a=int(input("digite um número: "))
b=int(input("digite outro número: "))

c=a+b

if a==b :
    print(" a soma dos números é:",c," e os números são iguais")
else:
    if a>b :
        print(" a soma dos números é:",c," e o primerio número é maior")
    else:
        print(" a soma dos números é:",c," e o segundo número é maior")